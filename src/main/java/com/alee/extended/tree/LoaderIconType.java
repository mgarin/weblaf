package com.alee.extended.tree;

/**
 * This enumeration represents various loader icon types.
 *
 * @author Mikle Garin
 * @since 1.4
 */

public enum LoaderIconType
{
    /**
     * Circle loader
     */
    circe,

    /**
     * Facebook loader
     */
    facebook,

    /**
     * Roller loader
     */
    roller,

    /**
     * Arrows loader
     */
    arrows,

    /**
     * Indicator loader
     */
    indicator,

    /**
     * Snake loader
     */
    snake,

    /**
     * Empty loader
     */
    none
}